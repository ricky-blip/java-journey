package com.example.javabasic.learn;

public class TipeDataNumber {
    public static void main(String[] args) {
        //Integer Number
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 10000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000L;
        //Floating Point Number
        float iniFloat = 10.10F;
        double iniDouble = 10.10;
        //Kode Literal
        int decimalInt = 25;
        int hexInt = 0xA132B;
        int binInt = 0b10101010;
        //Kode Underscore, untuk pemisah angka saja supaya bisa mudah dibaca
        int iniUnderscore = 1_000_000_000;
        System.out.println(iniUnderscore);
    }
}
