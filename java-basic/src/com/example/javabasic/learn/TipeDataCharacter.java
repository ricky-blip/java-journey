package com.example.javabasic.learn;

public class TipeDataCharacter {
    public static void main(String[] args) {
        //char hanya satu petik
        char r = 'R';
        char i = 'I';
        char c = 'C';
        char k = 'K';
        char y = 'Y';
        //println -> untuk enter
        System.out.println(r);
        System.out.println(i);
        System.out.println(c);
        System.out.println(k);
        System.out.println(y);
        //print -> tanpa enter
        System.out.print(r);
        System.out.print(i);
        System.out.print(c);
        System.out.print(k);
        System.out.print(y);
    }
}
